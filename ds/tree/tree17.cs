class Solution {
public:
	struct TreeNode {
		int val;
		TreeNode *left;
		TreeNode *right;
		TreeNode(int x) : val(x), left(NULL), right(NULL) {}
	};

public:
	vector<double> averageOfLevels(TreeNode* root) {
		vector<double> res;
		queue<TreeNode*> q;
		q.push(root);
		while (!q.empty()) {
			long temp = 0;
			int s = q.size();
			for (int i = 0; i<s; i++) {
				TreeNode* t = q.front();
				q.pop();
				if (t->left) q.push(t->left);
				if (t->right) q.push(t->right);
				temp += t->val;
			}
			res.push_back((double)temp / s);
		}
		return res;
	}
};
